
public class CustomerInformation extends Information {

	int dateOfBirth;
	String nationalityIdentity;
	
	public CustomerInformation(String firstName,String lastName,int dateOfBirth,String nationalityIdentity) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.dateOfBirth = dateOfBirth;
		this.nationalityIdentity = nationalityIdentity;
	}
	

	
}
